package Com.Flashnet.Model;

public class CreateCustomer {
    private String customerId;
    private String customerName;
    private String customerPhone;
    private String customerEmail;
    private String installationDate;
    private String customerAreaName;
    private String customerAddress;
    private String customerPackageId;
    private String customerSubAreaName;
    private double customerPackCost;

    public String getCustomerId() {
        return customerId;
    }

    public void setCustomerId(String customerId) {
        this.customerId = customerId;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getCustomerPhone() {
        return customerPhone;
    }

    public void setCustomerPhone(String customerPhone) {
        this.customerPhone = customerPhone;
    }

    public String getCustomerEmail() {
        return customerEmail;
    }

    public void setCustomerEmail(String customerEmail) {
        this.customerEmail = customerEmail;
    }

    public String getInstallationDate() {
        return installationDate;
    }

    public void setInstallationDate(String installationDate) {
        this.installationDate = installationDate;
    }

    public String getCustomerAreaName() {
        return customerAreaName;
    }

    public void setCustomerAreaName(String customerAreaName) {
        this.customerAreaName = customerAreaName;
    }

    public String getCustomerAddress() {
        return customerAddress;
    }

    public void setCustomerAddress(String customerAddress) {
        this.customerAddress = customerAddress;
    }

    public String getCustomerPackageId() {
        return customerPackageId;
    }

    public void setCustomerPackageId(String customerPackageId) {
        this.customerPackageId = customerPackageId;
    }

    public String getCustomerSubAreaName() {
        return customerSubAreaName;
    }

    public void setCustomerSubAreaName(String customerSubAreaName) {
        this.customerSubAreaName = customerSubAreaName;
    }
    
    

    public double getCustomerPackCost() {
        return customerPackCost;
    }

    public void setCustomerPackCost(double customerPackCost) {
        this.customerPackCost = customerPackCost;
    }
    
    
}
